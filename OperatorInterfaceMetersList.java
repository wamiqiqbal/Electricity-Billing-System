    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = OperatorInterfaceMetersList.getSelectedRow();

        if (selectedRow != -1) {
             // Deleting row from the database
             // JDBC Connection
        
             // URL, USERNAME, PASSWORD
             String url = "jdbc:mysql://localhost:3306/electricitybillingsystem";
             String username = "root";
             String password = "noemak";
    
             // Establishing Connection
            try {
                  Connection con = DriverManager.getConnection(url, username, password);
                  System.out.println("Connection Established Successfully!");
                  String meterNo = OperatorInterfaceMetersList.getValueAt(selectedRow, 0).toString();
                  String query = "DELETE FROM meters_list WHERE meter_number = " + meterNo + ";";
                  PreparedStatement prepStm = con.prepareStatement(query);
                  int rowsAffected = prepStm.executeUpdate();
           
                  prepStm.close();
                  con.close();
            
                } catch (SQLException excp) {
                    System.out.println(excp.getMessage());
                }              
            
            // Remove selected row
            model.removeRow(selectedRow);
        } else {
            // Show message if no row is selected
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

