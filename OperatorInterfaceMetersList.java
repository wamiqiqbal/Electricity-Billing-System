    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String meterNo, accNo;
        meterNo = txtMeterNo.getText();
        accNo = txtAccNo.getText();
        
        String[] data = {meterNo, accNo};

        // Adding values into the table
        model.addRow(data);

        // Setting fields empty
        txtMeterNo.setText("");
        txtAccNo.setText("");
        
        // Adding values to the database
        // JDBC Connection
        
        // URL, USERNAME, PASSWORD
        String url = "jdbc:mysql://localhost:3306/electricitybillingsystem";
        String username = "root";
        String password = "noemak";
    
        // Establishing Connection
        try {
            Connection con = DriverManager.getConnection(url, username, password);
            System.out.println("Connection Established Successfully!");
            String query = "INSERT INTO meters_list(meter_number, account_number) VALUES (?, ?);";
            PreparedStatement prepStm = con.prepareStatement(query);
            prepStm.setString(1, meterNo);
            prepStm.setString(2, accNo);
            prepStm.executeUpdate();
           
            prepStm.close();
            con.close();
            
        } catch (SQLException excp) {
            System.out.println(excp.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

